#sum()
# Add the all the elements in the collection
a=[1.2,2.3,3.4,4.5]
print(sum(a))
#Min()
# Min is used to find minimum value in the collection
print(min(a))
#Max()
# Max is used to find the maximum value in the collection
print(max(a))
#Len()
# len return number of elements in the collection
print(len(a))
#Avg
# Avg = sum /length
# Avg return average value of a collection
avg=sum(a)/len(a)
print(avg)



